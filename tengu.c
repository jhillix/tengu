/* Initializes the LCD. */
void init(void)
{
	/* Reset */
	PORTB |= 1 << RST;
	PORTB &= ~(1 << RST);
	PORTB |= 1 << RST;

	/* Switch to extended commands. */
	commandMode(0x21);

	/* Set value of Vop (controls contrast). */
	/*commandMode(0xE0);*/
	commandMode(0xB3);

	/* Set temperature coefficient. */
	commandMode(0x04);

	/* set bias mode to 1:48. */
	commandMode(0x14);

	/* Switch back to regular commands. */
	commandMode(0x20);

	/* Enable normal display (dark on light), horizontal addressing. */
	commandMode(0x0C);

	/* Erase all pixel on the DDRAM. */
	clear();

	cursorXY(0,5);
	putString("              ");
	cursorXY(0,6);
	putString("              ");
	cursorXY(0,0);
}

