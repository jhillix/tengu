void commandMode(char command)
{
	/* Select Command register. */
	PORTB &= ~(1 << D_C);

	/* Write in data bits. */
	bitBang(command);
}

/* Writes data to DDRAM to illuminate the pixels. */
void dataMode(char data)
{
	/* Select data register. */
	PORTB |= 1 << D_C;

	/* Write in data bits. */
	bitBang(data);
}


/* Sends data to the PCD8544 controller. */
void bitBang(char bits)
{
	for (int bitCount = 8; bitCount > 0; bitCount--)
		{
			/* Set Clock Idle level LOW. */
			PORTA &= ~(1 << SCK);

			if ((bits & 0x80) == 0x80) {
				PORTB |= 1 << SDIN;
			} else {
				PORTB &= ~(1 << SDIN);
			}
			/* Data is clocked on the rising edge of SCK. */
			PORTA |= 1 << SCK;

			/* Logical shift data by 1 bit left. */
			bits = bits << 1;
		}
}

/* Erase the DDRAM contents. */
void clear(void)
{
	/* Cursor Home. */
	cursorXY(0, 0);

	for (int ddram = 504; ddram > 0; ddram--) {
		/* 6*84 = 504 DDRAM addresses. */
		dataMode(0x00);
	}
}

/* Position cursor to X, Y. */
void cursorXY(char x, char y)
{
	/* Y axis */
	commandMode(0x40 | (y&0x07));

	/* X axis */
	commandMode(0x80 | (x&0x7f));
}

void putCharacter(char character)
{
	int tablept, count, char_column;

	if ((character < 0x20) || (character > 0x7f)) {
		/* Exit function if character is not found. */
		return;
	}
	/* Point to the columns of the character in the table. */
	tablept = ((5 * character) - 160);

	/* Draw the columns to print the character. */
	for (count = 5; count > 0; count--)
	{
		char_column = pgm_read_byte((table + tablept));
		dataMode(char_column);
		tablept++;
	}
	/* 1 pixel spacing per character. */
	dataMode(0x00);
}

void putString(const char *s)
{
	while(*s) {
		/* Points to one ASCII to be written one at a time. */
		putCharacter(*s++);
	}
}
