int main(void)
{
	/* Set-up registers as output. */
	DDRB |= 1 << RST | 1 << D_C | 1 << SDIN;
	DDRA |= 1 << SCK;
	
	/* Set-up registers as input and put 5V on the pins. */
	DDRA &= ~(1 << TOP) | ~(1 << BOTTOM) | ~(1 << FRONT) | ~(1 << BEHIND) | ~(1 << LEFT) | ~(1 << RIGHT);
	PORTA |= 1 << TOP | 1 << BOTTOM | 1 << FRONT | 1 << BEHIND | 1 << LEFT | 1 << RIGHT;
	
	/* Set-up interrupts. */
	MCUCR |= 1 << ISC01;																			// Only trigger event on falling edge
	GIMSK |= 1 << PCIE0;																			// Enable interrupts for the pins
	GIFR |= 1 << PCIF0;																				// Enable interrupts flag for the pins
	PCMSK0 |= 1 << PCINT0 | 1 << PCINT1 | 1 << PCINT2 | 1 << PCINT3 | 1 << PCINT4 | 1 << PCINT5;	// Select which pins will be interrupts

	/* Enable global interrupts. */
	sei();

	/* Initialize LCD. */
	init();
	
	/* Write some data... _F_O_R_E_V_E_R_ */
	while (1) {

		/* Show Eddy. */
		clear();
		for (int i = 0; i < 504; i++) {
			dataMode(pgm_read_byte(&eddy[i]));
		}
		_delay_ms(400);

		/* Prompt question. */
		clear();
		for (int j = 0; j < 504; j++) {
			dataMode(pgm_read_byte(&where[j]));
		}
		_delay_ms(400);
		
		/* Show soft labels. */
		clear();
		for (int k = 0; k < 504; k++) {
			dataMode(pgm_read_byte(&softlabels[k]));
		}
		_delay_ms(400);

		/* Now wait for an event, i.e. interrupt. When an event occurs 
		 * the MCU will jump to our service routine, event handler, and
		 * execute the code inside and return back here to main and 
		 * continue where it left-off until another event is fired. If
		 * there is no event then this code will repeat.
		 */
	}
}


